/// Convert filename to a readable title.
fn title_from_filename(filename: &str) -> String {
    filename
        .trim_end_matches(".md")
        .split('_')
        .map(|w| {
            let mut chars = w.chars();
            match chars.next() {
                None => String::new(),
                Some(c) => c.to_uppercase().to_string() + chars.as_str(),
            }
        })
        .collect::<Vec<_>>()
        .join(" ")
}

