use std::fs;
use std::path::Path;

/// Brain file names within .brain/
const BRAIN_FILES: &[&str] = &[
    "index.md",
    "context.md",
    "architecture.md",
    "memory.md",
    "changelog.md",
    "roadmap.md",
    "decisions.md",
    "prompt.md",
    "tree.md",
    "readme.md",
    "tasks.md",
];

/// Knowledge sub-files within .brain/knowledge/
const KNOWLEDGE_FILES: &[&str] = &[
    "modules.md",
    "functions.md",
    "api.md",
    "database.md",
    "models.md",
    "services.md",
];

/// Initialize the .brain/ directory structure with seed files.
pub fn init_brain(root: &Path) -> Result<(), String> {
    let brain_dir = root.join(".brain");
    let knowledge_dir = brain_dir.join("knowledge");

    // Create directories
    fs::create_dir_all(&knowledge_dir)
        .map_err(|e| format!("Failed to create .brain/ directory: {}", e))?;

    // Create brain files (only if they don't exist)
    for file in BRAIN_FILES {
        let path = brain_dir.join(file);
        if !path.exists() {
            let seed = seed_content(file);
            fs::write(&path, &seed)
                .map_err(|e| format!("Failed to create .brain/{}: {}", file, e))?;
        }
    }

    // Create knowledge files (only if they don't exist)
    for file in KNOWLEDGE_FILES {
        let path = knowledge_dir.join(file);
        if !path.exists() {
            let seed = format!(
                "# {}\n\n> Auto-generated by TreeC Neural Link\n\n",
                title_from_filename(file)
            );
            fs::write(&path, &seed)
                .map_err(|e| format!("Failed to create .brain/knowledge/{}: {}", file, e))?;
        }
    }

    Ok(())
}

/// Update the tree.md file with the latest Tree.md content.
pub fn update_tree(root: &Path, tree_content: &str) -> Result<(), String> {
    let brain_tree = root.join(".brain").join("tree.md");
    fs::write(&brain_tree, tree_content)
        .map_err(|e| format!("Failed to write .brain/tree.md: {}", e))
}

/// Write AI-generated content to a specific brain file.
pub fn update_brain_file(root: &Path, filename: &str, content: &str) -> Result<(), String> {
    let path = root.join(".brain").join(filename);

    // Ensure parent directory exists
    if let Some(parent) = path.parent() {
        fs::create_dir_all(parent).map_err(|e| format!("Failed to create directory: {}", e))?;
    }

    fs::write(&path, content).map_err(|e| format!("Failed to write .brain/{}: {}", filename, e))
}

